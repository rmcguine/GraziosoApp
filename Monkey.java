public class Monkey extends RescueAnimal {
	
	// instance variables
	public double monkeyTailLength;
	public double monkeyHeight;
	public double monkeyBodyLength;
	public String monkeySpecies;
	public double monkeyTorso;
	public double monkeySkull;
	public double monkeyNeck;
	
	// Constructor
	public Monkey() {	
	}
	
	// Accessor And Mutators
	public double getMonkeyTailLength() {
		return monkeyTailLength;
	}
	public void setMonkeyTailLength(double monkeyTailLength) {
		this.monkeyTailLength = monkeyTailLength;
	}
	public double getMonkeyHeight() {
		return monkeyHeight;
	}
	public void setMonkeyHeight(double monkeyHeight) {
		this.monkeyHeight = monkeyHeight;
	}
	public double getMonkeyBodyLength() {
		return monkeyBodyLength;
	}
	public void setMonkeyBodyLength(double monkeyBodyLength) {
		this.monkeyBodyLength = monkeyBodyLength;
	}
	public String getMonkeySpecies() {
		return monkeySpecies;
	}
	public void setMonkeySpecies(String monkeySpecies) {
		this.monkeySpecies = monkeySpecies;
	}
	public double getMonkeyTorso() {
		return monkeyTorso;
	}
	public void setMonkeyTorso(double monkeyTorso) {
		this.monkeyTorso = monkeyTorso;
	}
	public double getMonkeySkull() {
		return monkeySkull;
	}
	public void setMonkeySkull(double monkeySkull) {
		this.monkeySkull = monkeySkull;
	}
	public double getMonkeyNeck() {
		return monkeyNeck;
	}
	public void setMonkeyNeck(double monkeyNeck) {
		this.monkeyNeck = monkeyNeck;
	}
	
	
	
}
